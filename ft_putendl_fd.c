#include <unistd.h>

void ft_putendl_fd(char *s, int fd)
{
	if(*s == 0)
		return;
	while(*s)
	{
		write(fd, s, 1);
		s++;
	}
	write(fd, "\n", 1);
}

int main()
{
	ft_putendl_fd("hello", 1);
}
